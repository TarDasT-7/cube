    public function send(Request $request)
    {
        $request->validate([

            'email'=>'required|email|max:250',
            'text'=>'required|min:5|max:5000',

        ]);


        $message=new Message();
        $message->user_id=$request->user;
        $message->email=$request->email;
        $message->text=$request->text;
        if($message->save())
        {
            return 0;

        }else
        {
            return 1;
        }

    }
